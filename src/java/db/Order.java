package db;

/**
 *
 * @author ASUS
 */
public class Order {
    private int id;
    private String date;
    private int user;
    private double totalMoney;

    public Order() {
    }

    public Order(int id, String date, int user, double totalMoney) {
        this.id = id;
        this.date = date;
        this.user = user;
        this.totalMoney = totalMoney;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public int getUser() {
        return user;
    }

    public void setUser(int user) {
        this.user = user;
    }

    public double getTotalMoney() {
        return totalMoney;
    }

    public void setTotalMoney(double totalMoney) {
        this.totalMoney = totalMoney;
    }
    
}
