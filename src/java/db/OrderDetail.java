package db;

/**
 *
 * @author ASUS
 */
public class OrderDetail {
    private int oid;
    private String tId;
    private int quantity;
    private double price;

    public OrderDetail() {
    }

    public OrderDetail(int oid, String tId, int quantity, double price) {
        this.oid = oid;
        this.tId = tId;
        this.quantity = quantity;
        this.price = price;
    }

    public int getOid() {
        return oid;
    }

    public void setOid(int oid) {
        this.oid = oid;
    }

    public String gettId() {
        return tId;
    }

    public void settId(String tId) {
        this.tId = tId;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }
    
    
    
}
